"""50 test scenarios for the approach-comparison benchmark."""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class ScenarioMetadata:
    """Token/cost/latency metadata for a scenario."""

    total_tokens: int
    cost_usd: float
    latency_ms: int


@dataclass
class Scenario:
    """Single benchmark test case."""

    id: str
    name: str
    category: str
    user_message: str
    tool_sequence: list[str]
    required_tools: list[str]
    forbidden_tools: list[str]
    output_message: str
    output_keywords: list[str]
    forbidden_terms: list[str]
    output_schema: dict[str, object]
    judge_criteria: list[str]
    metadata: ScenarioMetadata
    tool_args_overrides: dict[str, dict[str, object]] = field(default_factory=dict)


# ---------------------------------------------------------------------------
# Output schema shared across most scenarios
# ---------------------------------------------------------------------------

_BASE_OUTPUT_SCHEMA: dict[str, object] = {
    "type": "object",
    "properties": {
        "intent": {"type": "string"},
        "status": {"type": "string"},
    },
    "required": ["intent", "status"],
}


def _meta(tokens: int = 320, cost: float = 0.008, latency: int = 1200) -> ScenarioMetadata:
    return ScenarioMetadata(total_tokens=tokens, cost_usd=cost, latency_ms=latency)


